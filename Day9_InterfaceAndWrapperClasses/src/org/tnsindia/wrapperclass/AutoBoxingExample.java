package org.tnsindia.wrapperclass;

public class AutoBoxingExample {

	public static void main(String[] args) {
		//Conversion of primitive data types of its corresponding wrapper class
		char ch='z';
		
		//auto-boxing
		Character c=ch;
		
		
		//Character c1=new Character('x');
		
		System.out.println(ch+" "+c);

	}

}
